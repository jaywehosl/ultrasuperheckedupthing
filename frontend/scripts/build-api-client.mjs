#!/usr/bin/env node
// Generates src/generated/client.ts — a complete, typed catalog of every
// backend endpoint the panel can call, grouped by domain. Source of truth is
// src/pages/api-docs/endpoints.ts (the same `sections` that drive openapi.json),
// so the client can never drift from the documented contract.
//
// Run: npm run gen:client  (also folded into `npm run build`).
import { writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

import { sections } from '../src/pages/api-docs/endpoints.ts';

const __dirname = dirname(fileURLToPath(import.meta.url));
const outPath = join(__dirname, '..', 'src', 'generated', 'client.ts');

// ---- helpers ---------------------------------------------------------------

function camel(parts) {
  return parts
    .filter(Boolean)
    .map((p, i) =>
      i === 0
        ? p.charAt(0).toLowerCase() + p.slice(1)
        : p.charAt(0).toUpperCase() + p.slice(1),
    )
    .join('');
}

// "custom-geo" -> "customGeo", "api-docs" -> "apiDocs"
function sectionVar(id) {
  return camel(id.split(/[-_\s]+/)) + 'Api';
}

// Paths in endpoints.ts use gin syntax (":email"); also tolerate "{email}".
function extractPathParams(path) {
  const out = [];
  const re = /[:{]([A-Za-z_][A-Za-z0-9_]*)\}?/g;
  let m;
  while ((m = re.exec(path)) !== null) out.push(m[1]);
  return out;
}

// Derive a readable method name from the path.
function methodName(path) {
  let p = path;
  if (p.startsWith('/panel/api/')) {
    p = p.slice('/panel/api/'.length);
    // drop the leading domain segment (clients, inbounds, ...)
    const slash = p.indexOf('/');
    p = slash >= 0 ? p.slice(slash + 1) : p;
  } else if (p.startsWith('/')) {
    p = p.slice(1);
  }
  const segments = [];
  const trailingParams = [];
  for (const seg of p.split('/')) {
    if (!seg) continue;
    const pm = seg.match(/^\{([A-Za-z_][A-Za-z0-9_]*)\}$/) || seg.match(/^:([A-Za-z_][A-Za-z0-9_]*)$/);
    if (pm) trailingParams.push(pm[1]);
    else segments.push(seg.replace(/[^A-Za-z0-9]+/g, ' ').trim().split(/\s+/));
  }
  const flatStatic = segments.flat();
  const byParts = trailingParams.map((pn) => 'By ' + pn);
  const name = camel([...flatStatic, ...byParts.flatMap((s) => s.split(' '))]);
  return name || 'root';
}

const TS_TYPE = {
  string: 'string',
  integer: 'number',
  number: 'number',
  boolean: 'boolean',
  'integer[]': 'number[]',
  object: 'Record<string, unknown>',
  'object[]': 'unknown[]',
  array: 'unknown[]',
  file: 'File | Blob',
};

function tsType(t) {
  return TS_TYPE[t] || 'unknown';
}

function isBody(loc) {
  return loc === 'body' || loc.startsWith('body');
}

// ---- code generation -------------------------------------------------------

const header = `// Code generated by scripts/build-api-client.mjs. DO NOT EDIT.
// Source of truth: src/pages/api-docs/endpoints.ts
//
// One typed function per backend endpoint, grouped by domain. Path params are
// positional args; query/body params are a single typed object. Every call
// returns the standard \`Msg<T>\` envelope. Response payloads are \`unknown\` by
// design — validate them at the call site with the matching zod schema in
// src/schemas/. Pass a type argument to narrow: \`clientsApi.list<ClientRecord[]>()\`.
/* eslint-disable @typescript-eslint/no-explicit-any */
import { HttpUtil, type Msg, type HttpOptions } from '@/utils';

const JSON_HEADERS: HttpOptions = { headers: { 'Content-Type': 'application/json' } };

function enc(v: string | number): string {
  return encodeURIComponent(String(v));
}
`;

const blocks = [];

for (const section of sections) {
  const fns = [];
  const usedNames = new Map();

  for (const ep of section.endpoints) {
    if (ep.method === 'WS') continue; // websockets handled separately

    const pathParams = extractPathParams(ep.path);
    const params = ep.params || [];
    const queryParams = params.filter((p) => p.in === 'query');
    const bodyParams = params.filter((p) => isBody(p.in));
    const jsonBody = params.some((p) => p.in === 'body (json)') || ep.method !== 'GET' && bodyParams.length === 0 && ep.body;
    const multipart = params.some((p) => p.in === 'body (multipart)');

    // unique name within the section (prefix with http method on collision)
    let name = methodName(ep.path);
    if (usedNames.has(name)) {
      name = camel([ep.method.toLowerCase(), name]);
    }
    let dedupe = name;
    let n = 2;
    while (usedNames.has(dedupe)) dedupe = name + n++;
    name = dedupe;
    usedNames.set(name, true);

    // collect args as {name, type, optional}; path params are always required
    const argSpecs = [];
    for (const pp of pathParams) argSpecs.push({ name: pp, type: 'string | number', optional: false });

    let bodyArgName = null;
    if (multipart) {
      bodyArgName = 'form';
      argSpecs.push({ name: 'form', type: 'FormData', optional: false });
    } else if (bodyParams.length > 0) {
      const fields = bodyParams
        .map((p) => `${p.name}${p.optional ? '?' : ''}: ${tsType(p.type)}`)
        .join('; ');
      bodyArgName = 'body';
      argSpecs.push({ name: 'body', type: `{ ${fields} }`, optional: false });
    } else if (ep.method !== 'GET') {
      bodyArgName = 'body';
      argSpecs.push({ name: 'body', type: 'unknown', optional: true });
    }

    let queryArgName = null;
    if (queryParams.length > 0) {
      const fields = queryParams
        .map((p) => `${p.name}${p.optional ? '?' : ''}: ${tsType(p.type)}`)
        .join('; ');
      queryArgName = 'params';
      argSpecs.push({ name: 'params', type: `{ ${fields} }`, optional: queryParams.every((p) => p.optional) });
    } else if (ep.method === 'GET') {
      queryArgName = 'params';
      argSpecs.push({ name: 'params', type: 'Record<string, unknown>', optional: true });
    }

    argSpecs.push({ name: 'options', type: 'HttpOptions', optional: true });

    // no optional arg may precede a required one (TS): promote from the right
    for (let i = argSpecs.length - 1, reqSeen = false; i >= 0; i--) {
      if (!argSpecs[i].optional) reqSeen = true;
      else if (reqSeen) argSpecs[i].optional = false;
    }
    const args = argSpecs.map((a) => `${a.name}${a.optional ? '?' : ''}: ${a.type}`);

    // build URL expression (supports both ":param" and "{param}")
    const urlExpr = pathParams.length
      ? '`' + ep.path.replace(/[:{]([A-Za-z_][A-Za-z0-9_]*)\}?/g, (_, p) => '${enc(' + p + ')}') + '`'
      : `'${ep.path}'`;

    const summary = (ep.summary || '').replace(/\*\//g, '*\\/').slice(0, 240);
    const depr = ep.deprecated ? '\n     * @deprecated' : '';

    let callArgs;
    let opts;
    if (ep.method === 'GET') {
      opts = 'options';
      callArgs = `${urlExpr}, ${queryArgName}, ${opts}`;
      fns.push(
        `  /** ${summary}${depr} */\n` +
          `  ${name}<T = unknown>(${args.join(', ')}): Promise<Msg<T>> {\n` +
          `    return HttpUtil.get<T>(${callArgs});\n` +
          `  },`,
      );
    } else {
      const optionParts = [];
      if (jsonBody && !multipart) optionParts.push('...JSON_HEADERS');
      if (queryArgName) optionParts.push(`params: ${queryArgName}`);
      const mergeOpts = optionParts.length ? `{ ${optionParts.join(', ')}, ...options }` : 'options';
      callArgs = `${urlExpr}, ${bodyArgName || 'undefined'}, ${mergeOpts}`;
      const verb = 'post';
      fns.push(
        `  /** ${summary}${depr} */\n` +
          `  ${name}<T = unknown>(${args.join(', ')}): Promise<Msg<T>> {\n` +
          `    return HttpUtil.${verb}<T>(${callArgs});\n` +
          `  },`,
      );
    }
  }

  if (fns.length === 0) continue;
  blocks.push(
    `/** ${section.title} */\nexport const ${sectionVar(section.id)} = {\n${fns.join('\n\n')}\n};`,
  );
}

const out = header + '\n' + blocks.join('\n\n') + '\n';
writeFileSync(outPath, out, 'utf8');
console.log(`Wrote ${outPath} (${blocks.length} domains)`);
